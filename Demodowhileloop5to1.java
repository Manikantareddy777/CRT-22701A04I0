class Demodowhileloop5to1
{
	public static void main(String[] args)
	{
		int i=5;
		int j=5;
		while(j>=1)
		{
			System.out.println(i);
			j--;
		}
		System.out.println("___________________________");
		do
		{
			System.out.println(i);
			i--;
			
			
		}
		while(i>=1);
		System.out.println("___________________________");
		
		
		
	}
}