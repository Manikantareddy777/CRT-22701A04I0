class Demoifelseif
{
	public static void main(String[] args)
	{
		int a=10;
		int b=10;
		if(a<b)
		{
			System.out.println("a is lesser than b");
		}
		else if(a>b)
		{
			System.out.println("a is greater than a");
		}
		else if(a==b)
		{
			System.out.println("a is equal to b");
		}
		else
		{
			System.out.println("something wrong.........");
		}
	}
			
			
}