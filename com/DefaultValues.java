package com;

class DefaultValues {
	int a;
	char b;
	boolean c;
	String d;
	double e;
	public static void main(String[] args) {
		DefaultValues d=new DefaultValues();
		System.out.println(d.a);
		System.out.println(d.b);
		System.out.println(d.c);
		System.out.println(d.d);
		System.out.println(d.e);
		
		
	}

}
